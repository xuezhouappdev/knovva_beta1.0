<script>
    var overlayDiv = $(".overlay");
    var animationText = $(".inner-text.animation-text");

    var slider20Top = $("#slider20-top");


        $.scrollify({

            section: ".vertical-slider-item",
            sectionName: "section-name",
            interstitialSection: ".social",
            easing: null,
            scrollSpeed: 600,
            offset: 0,
            scrollbars: false,
            standardScrollElements: "",
            setHeights: true,
            overflowScroll: true,
            updateHash: true,
            touchScroll: true,
            before: function (index, sections) { //before entering the section


                if(index==11 || index==12 || index==13) {
                    TweenMax.set(overlayDiv, {visibility:"hidden"});
                    TweenMax.set(animationText, {visibility:"hidden"});
                }


                //the 21st slider
                if(index ==19) {
                    TweenMax.set(slider20Top, {visibility:"visible",autoAlpha:"1"});
                }//19 ends


                //pagination controll
                var ref = sections[index].attr("data-section-name");
                $(".pagination .active").removeClass("active");
                $(".pagination").find("a[href=\"#" + ref + "\"]").addClass("active");


            },
            after: function (index,sections) { //after entering the section

                if(index==11 || index==12 || index==13) {


                    var tlFrame = new TimelineMax({delay:1});

                    tlFrame
                       .to(overlayDiv,1.5,{
                           autoAlpha:1
                    },)
                        .to(animationText,1,{
                            autoAlpha:1
                        });

                } //ends

                if(index ==19) {
                    var fadeInTL = new TimelineMax({delay:1});
                    fadeInTL.to(slider20Top,2,{
                        autoAlpha:0
                    });
                }//19 ends
            },
            afterResize: function () {
            },
            afterRender:function (){
                var pagination = "<ul class=\"pagination\" style='margin-top: 10px'>";
                var activeClass = "";
                $(".vertical-slider-item").each(function(i) {
                    activeClass = "";
                    if(i===0) {
                        activeClass = "active";
                    }
                    pagination += "<li><a class=\"" + activeClass + "\" href=\"#" + $(this).attr("data-section-name") + "\">" + "<i class=\"fa fa-circle\" aria-hidden=\"true\"></i>"  + "</a></li>";
                });

                pagination += "</ul>";

                $(".vertical-slider-wrapper").append(pagination);
                $(".pagination a").on("click",$.scrollify.move);
            }
        });





        //typing effect for #2 slide
        var options = {
            strings: ["&nbsp;", "&nbsp;What matters? ^3000 "],
            typeSpeed: 50,
            fadeOut: true,
            loop: true,
            showCursor: true
        };

        var typed = new Typed(".textChange", options);

        //typing effect for #25 slide
        var options2 = {
            strings: ["&nbsp;", "&nbsp;In the end... ^3000 "],
            typeSpeed: 50,
            fadeOut: true,
            loop: true,
            showCursor: true
        };

        var typed2 = new Typed(".endChange", options2);


